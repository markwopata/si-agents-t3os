WITH COMPANY_GEOFENCES AS (
  SELECT company_id, geofence_id
  FROM es_warehouse.public.geofences
  WHERE 
    deleted = FALSE --NOT DELETED, AKA ACTIVE
    AND date_created::date <= current_date --NOT IN THE FUTURE 
  GROUP BY ALL 
),

OWNED_OR_RENTED_ASSETS AS (
  SELECT company_id, asset_id, NULL::DATE as start_date, NULL::DATE as end_date, 'OWNED' AS OWNERSHIP 
  FROM business_intelligence.triage.stg_t3__asset_info
  GROUP BY ALL 

  UNION 

  SELECT company_id, asset_id, rental_start_date as start_date, SCHEDULED_OFF_RENT_DATE as end_date, 'CURRENT RENTAL' AS OWNERSHIP
  FROM business_intelligence.triage.stg_t3__on_rent
  WHERE ASSET_ID IS NOT NULL 

  UNION

  SELECT rental_company_id as company_id, asset_id, start_date, end_date, 'PAST RENTAL' AS OWNERSHIP
  FROM business_intelligence.triage.stg_t3__company_values
  WHERE rental_id IS NOT NULL 
  -- FILTERS TO PAST RENTALS:
  AND ASSET_ID NOT IN (SELECT ASSET_ID FROM business_intelligence.triage.stg_t3__on_rent WHERE ASSET_ID IS NOT NULL GROUP BY 1)
  GROUP BY ALL 
),

ASSET_GEOFENCE_UNIVERSE AS 
(
SELECT 
    CG.COMPANY_ID,
    H.GEOFENCE_ID, 
    H.ASSET_ID, 
    H.report_range:start_range::DATE AS USAGE_DATE, 
    ORA.OWNERSHIP,
    -- SHOULD BE MAX OF 1 HOUR:
    SUM(CASE WHEN H.HOURS > 1 THEN 1 ELSE H.HOURS END) AS HOURS_IN_GEOFENCE
FROM es_warehouse.public.hourly_asset_geofence_usage H
INNER JOIN company_geofences CG USING (geofence_id)
INNER JOIN OWNED_OR_RENTED_ASSETS ORA 
    ON H.ASSET_ID = ORA.ASSET_ID
    AND CG.COMPANY_ID = ORA.COMPANY_ID
    AND (
        ORA.START_DATE IS NULL 
        OR H.report_range:start_range::DATE BETWEEN ORA.START_DATE AND ORA.END_DATE 
    ) 
-- LAST 365 DAYS:
WHERE H.report_range:start_range::DATE >= CURRENT_DATE-365
-- REMOVES BAD NEGATIVE HOUR DATA:
AND H.HOURS > 0 
GROUP BY ALL 
),


TOTAL_GEOFENCES AS 
(
SELECT * FROM ASSET_GEOFENCE_UNIVERSE

UNION 

-- JOINS IN CURRENT RENTALS WITH 0 GEOFENCE USAGE TO DATE:

SELECT COMPANY_ID, NULL AS GEOFENCE_ID, ASSET_ID, NULL::DATE AS USAGE_DATE, 'CURRENT RENTAL' AS OWERNSHIP, NULL AS HOURS_IN_GEOFENCE 
FROM business_intelligence.triage.stg_t3__on_rent
WHERE COMPANY_ID IN (SELECT COMPANY_ID FROM ASSET_GEOFENCE_UNIVERSE GROUP BY 1)
AND ASSET_ID NOT IN (SELECT ASSET_ID FROM ASSET_GEOFENCE_UNIVERSE GROUP BY 1)

-- UNION 

